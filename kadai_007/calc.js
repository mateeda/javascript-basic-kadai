num=Math.floor(15);

console.log(num);

switch(num){
    case 6:
        console.log('3の倍数です');
        break;
    case 10:
        console.log('5の倍数です');
        break;
    case 15:
        console.log('3と5の倍数です');
        break;
    default:
        console.log(num);
        break;
}